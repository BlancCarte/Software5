package user;

public class Dish {
	private int steak;
	private int pasta;
	private int salad;
	private int chips;
	private int soda;
	private int juice;
	private int chocolate;
	private int cheese;
	
	public int getSteak() {
		return steak;
	}
	public void setSteak(int steak) {
		this.steak = steak;
	}
	public int getPasta() {
		return pasta;
	}
	public void setPasta(int pasta) {
		this.pasta = pasta;
	}
	public int getSalad() {
		return salad;
	}
	public void setSalad(int salad) {
		this.salad = salad;
	}
	public int getChips() {
		return chips;
	}
	public void setChips(int chips) {
		this.chips = chips;
	}
	public int getSoda() {
		return soda;
	}
	public void setSoda(int soda) {
		this.soda = soda;
	}
	public int getJuice() {
		return juice;
	}
	public void setJuice(int juice) {
		this.juice = juice;
	}
	public int getChocolate() {
		return chocolate;
	}
	public void setChocolate(int chocolate) {
		this.chocolate = chocolate;
	}
	public int getCheese() {
		return cheese;
	}
	public void setCheese(int cheese) {
		this.cheese = cheese;
	}
}
